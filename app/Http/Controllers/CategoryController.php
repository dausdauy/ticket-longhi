    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'kategori'=>'required'
        ]);
        
        $data = new Category([
            'name_categories'=>$request->get('kategori'),
        ]);

        $data->save();

        return redirect()->back()->with('success', $request->get('kategori').' has been Added.');
    }

